package com.winash.uniapp;

public class MarksApplicantUpdation {

    public String m10,m12,mug,mpg;

    public MarksApplicantUpdation(String m10, String m12, String mug, String mpg) {
        this.m10 = m10;
        this.m12 = m12;
        this.mug = mug;
        this.mpg = mpg;
    }

    public String getM10() {
        return m10;
    }

    public void setM10(String m10) {
        this.m10 = m10;
    }

    public String getM12() {
        return m12;
    }

    public void setM12(String m12) {
        this.m12 = m12;
    }

    public String getMug() {
        return mug;
    }

    public void setMug(String mug) {
        this.mug = mug;
    }

    public String getMpg() {
        return mpg;
    }

    public void setMpg(String mpg) {
        this.mpg = mpg;
    }
}
